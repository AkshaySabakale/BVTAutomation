namespace Dsa.DataModels
{
    public class Contact
    {
        /// <summary>
        /// Gets or sets the invoice email.
        /// </summary>
        /// <value>
        /// The invoice email.
        /// </value>
        public string EmailInvoice { get; set; }

        /// <summary>
        /// Gets or sets the Fax.
        /// </summary>
        /// <value>
        /// The Fax.
        /// </value>
        public string Fax { get; set; }

        /// <summary>
        /// Gets or sets the Mobile Phone.
        /// </summary>
        /// <value>
        /// The Mobile Phone.
        /// </value>
        public string PhoneMobile { get; set; }

        /// <summary>
        /// Gets or sets the Work Phone.
        /// </summary>
        /// <value>
        /// The Work Phone.
        /// </value>
        public string PhoneWork { get; set; }

        /// <summary>
        /// Gets or sets the Work Phone Extension.
        /// </summary>
        /// <value>
        /// The Work Phone Extension.
        /// </value>
        public string PhoneWorkExtension { get; set; }
    }
}
